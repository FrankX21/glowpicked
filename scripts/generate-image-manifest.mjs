#!/usr/bin/env node
/**
 * Génère public/js/product-images.js à partir de data/analyses/*.json
 * Exécuté automatiquement via le script "prebuild" dans package.json.
 *
 * Output: window.GLOW_IMAGES = { 'ASIN': 'https://...cdn...', ... }
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ANALYSES_DIR = path.join(__dirname, '../site/data/analyses');
const OUT_DIR     = path.join(__dirname, '../site/public/js');
const OUT_FILE    = path.join(OUT_DIR, 'product-images.js');

const files = fs.readdirSync(ANALYSES_DIR).filter(f => f.endsWith('.json'));
const map = {};
let count = 0;

for (const file of files) {
  try {
    const data = JSON.parse(fs.readFileSync(path.join(ANALYSES_DIR, file), 'utf8'));
    if (data.asin && data.imageUrl) {
      map[data.asin] = data.imageUrl;
      count++;
    }
  } catch { /* ignore malformed */ }
}

fs.mkdirSync(OUT_DIR, { recursive: true });
fs.writeFileSync(
  OUT_FILE,
  `/* Auto-generated by generate-image-manifest.mjs — do not edit */\nwindow.GLOW_IMAGES=${JSON.stringify(map)};\n`
);

console.log(`[GlowPicked] Image manifest: ${count} produits → public/js/product-images.js`);
